#!/bin/bash
sudo apt update # refresh the list of available packages
sudo apt install xdotool # minimizes windows that may pop up
sudo apt-get install -y wmctrl # allows list of windows to be made available
sudo apt-get install -y xterm # simple terminal interface
sudo apt-get install -y wget # tool to check for ping connections
sudo apt install gnome-tweaks # visual tweaks for gnome terminal 
sudo apt-get -y install gnome-icon-theme # fixes an icon error on gnome terminal
sudo apt install chrome-gnome-shell # used to install gnome extensions

